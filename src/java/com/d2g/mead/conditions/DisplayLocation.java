package com.d2g.mead.conditions;

public class DisplayLocation {

	private String full;
	private String city;
	private String state;
	private String zip;
	
	public DisplayLocation() {}

	public String getFull() {
		return full;
	}

	public String getCity() {
		return city;
	}

	public String getState() {
		return state;
	}

	public String getZip() {
		return zip;
	}
	
	
}
