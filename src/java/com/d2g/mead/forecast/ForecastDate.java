package com.d2g.mead.forecast;

public class ForecastDate {

	private int day;
	private String monthname;
	private String weekday;
	
	public ForecastDate() {}

	public int getDay() {
		return day;
	}

	public String getMonthName() {
		return monthname;
	}

	public String getWeekday() {
		return weekday;
	}

	
}
